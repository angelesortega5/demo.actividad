package demo.actividad;

public class Gato {

	String color;
	int colas;
	Double peso;

	public void setColor(String color) {
		this.color = color;
	}

	public String getColor() {
		return this.color;
	}

	public void setColas(int colas) {
		this.colas = colas;
	}

	public int getColas() {
		return this.colas;

	}

	public void setPeso(Double peso) {
		this.peso = peso;

	}

	public Double getPeso() {
		return this.peso;
	}

}
