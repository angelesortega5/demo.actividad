package demo.actividad;

public class Perro {

	String nombre;
	int peso;
	Double altura;

	public void setnombre(String nombre) {
		this.nombre = nombre;

	}

	public String getnombre() {
		return this.nombre;
	}

	public void setpeso(int peso) {
		this.peso = peso;

	}

	public int getpeso() {
		return this.peso;
	}

	public void setaltura(Double altura) {
		this.altura = altura;
	}

	public Double getaltura() {
		return this.altura;
	}

}
